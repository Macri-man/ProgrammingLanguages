public class MyClass {
  // Fields (attributes) of the class
  int myNumber;
  String myString;

  // Constructor
  public MyClass(int number, String text) {
    myNumber = number;
    myString = text;
  }

  // Method
  public void display() {
    System.out.println("Number: " + myNumber);
    System.out.println("String: " + myString);
  }
}
